#Shortcut operator tambah
a = 10
a += 5  # a = a + 5
print("Hasil a setelah penambahan:", a)
#Shortcut operator kurang
b = 20
b -= 8  # b = b - 8
print("Hasil b setelah pengurangan:", b)
#Shortcut operator kali
c = 4
c *= 3  # c = c * 3
print("Hasil c setelah perkalian:", c)
#Shortcut operator bagi
d = 15
d /= 5  # d = d / 5
print("Hasil d setelah pembagian:", d)
#Shortcut operator modulus
e = 17
e %= 3  # e = e % 3
print("Hasil e setelah modulus:", e)    
#shortcut operator pangkat
f = 2
f **= 3  # f = f ** 3
print("Hasil f setelah pangkat:", f)
#shortcut operator floor division
g = 10
g //= 3  # g = g // 3
print("Hasil g setelah floor division:", g)
